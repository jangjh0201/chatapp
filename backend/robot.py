import time


class Robot:
    """
    로봇 클래스는 음료 만들기와 서랍 빼기, 서랍 정리 작업을 수행하는 역할을 담당합니다.
    """

    def make_drink(self):
        """
        음료 만들기 작업을 수행합니다.
        """
        print("로봇: 음료를 만들기 시작합니다.")
        time.sleep(15)  # 음료 만들기 작업 시뮬레이션
        print("로봇: 음료가 완성되었습니다.")
        return "음료가 완성되었어요."

    def get_items(self):
        """
        서랍에서 물건들을 꺼내는 작업을 수행합니다.
        """
        print("로봇: 서랍에서 물건들을 꺼내는 중입니다.")
        time.sleep(45)  # 서랍 빼기 작업 시뮬레이션
        print("로봇: 물건들을 모두 꺼냈습니다.")
        return "물건들을 모두 꺼냈어요."

    def put_items(self):
        """
        서랍에 물건들을 넣는 작업을 수행합니다.
        """
        print("로봇: 서랍에 물건들을 넣는 중입니다.")
        time.sleep(25)  # 서랍 정리 작업 시뮬레이션
        print("로봇: 물건들을 모두 정리했습니다.")
        return "물건들을 모두 정리했어요."
